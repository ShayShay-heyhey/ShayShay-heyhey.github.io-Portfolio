<h1>Lyre Reconstruction</h1>

<p>For my birthday, I was given a lyre, but it looked nothing like the ones I’d seen in games or films. Curious, I researched ancient lyres and discovered how these once-common instruments have nearly disappeared, surviving mostly through artifacts and recreations. To experience their original sound and form, I designed and built my own based on the Trossingen and Sutton Hoo lyres.</p>

<!-- Move CNC1.JPG to static/images/ -->
<img src="/images/SuttonhooLyre.jpg" alt="CNC Process" class="image" />
<p class=sub>Sutton Hoo Lyre</p>

<p>I researched the dimensions of ancient lyres and developed a detailed 3D model based on historical references. After finalizing the digital design, I sourced a large piece of walnut from a local wood supplier recommended for instrument construction.</p>
<img src="/images/T_Lyre3d.2.png" alt="CNC Process" class="image" />
<p class=sub>Lyre Body 3D Model</p>

<p>Using my school’s makerspace, I prepared the material with a planer, drill press, and band saw before modeling a CNC toolpath to carve the hollow body. The two halves were then clamped and glued together to form the final structure.</p>
<img src="/images/Lyre2.jpg" alt="CNC Process" class="imageL" />
<p class=sub>Glued Lyre Body</p>

<p>I 3D-modeled and printed the tuning pegs, bridge, and tailpiece, and completed the instrument with nylon harp strings. The finished lyre combines traditional craftsmanship with modern digital fabrication techniques to recreate the sound from the past.</p>
<img src="/images/Lyre1.JPG" alt="CNC Process" class="image" />
<p class=sub>The Completed Lyre</p>

