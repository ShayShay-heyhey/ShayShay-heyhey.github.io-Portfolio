<h1>Creative Coding</h1>


<p>I have developed a strong interest in creative coding, particularly using p5.js, a JavaScript library designed for visual and interactive art. It is widely used by artists, designers, and programmers to explore the intersection of art, mathematics, and computation.</p>

<p>Using p5.js, I developed a program that converts images into ASCII art generated from user-provided text.</p>

<img src="/images/p5js_edit.png" alt="Original photograph before ASCII conversion" class="imageL"/>
<p class=sub>P5js Editor</p>

<p>The program allows users to input both an image and a custom text string. The image is analyzed for brightness values, and the program reconstructs the image by placing characters from the input text in positions that correspond to the image’s light and dark areas.</p>

<img src="/images/TOP_OG.jpg" alt="Original photograph before ASCII conversion" class="image"/>
<p class=sub>Image Input</p>

<p>This creates a visual representation of the image entirely composed of text characters.</p>

<img src="/images/TOP_Song_SVG.png" alt="ASCII art conversion result" class="image"/>
<p class=sub>Image output</p>

<p>I went even further and programmed the software to export the images as svg's. This allows me to program machines such as CNC's, 3D printers and pen plotters to draw the image. For this instance I used my Unnatek pen plotter.</p>

