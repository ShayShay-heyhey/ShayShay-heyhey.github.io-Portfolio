<h1>MUSE Research</h1>

<p>During the summer of 2023, I was selected to conduct research with Dr. Riccardi through the Mentored Undergraduate Summer Experience (MUSE) at The College of New Jersey. Our work explored ancient Roman coinage and imperial portraiture as a way of understanding how Roman authority was visually communicated across the empire.</p>

<img src="/images/MUSE_coin.jpg" alt="CNC Process" class="imageL" />
<p class=sub>Coin Portrait Comparison</p>

<p>My research focused on coins depicting the Roman emperor Trebonianus Gallus. I cataloged and classified the different portrait styles found on these coins and analyzed the archaeological sites where they were discovered. By comparing variations in portraiture with their geographic distribution, I developed theories about how imperial imagery may have been interpreted and adapted in different provinces of the Roman Empire.</p>

<p>Part of this research required working with rare scholarly resources at the special collections of Princeton University Library – Firestone Library, where I conducted archival research using historical numismatic references and primary materials.</p>
<img src="/images/Firestone.jpg" alt="CNC Process" class="image" />
<p class=sub>Princeton University Library – Firestone Library</p>

<p>The project culminated in a presentation at the MUSE poster session titled “The Roman Emperor Seen from the Provinces: Imaging Roman Power in the Cities of the Empire (31 B.C.–A.D. 297).” The work was also highlighted in a feature <a href= "https://artscomm.tcnj.edu/2023/10/26/muse-students-explore-emperors-through-roman-coins/">article</a> published by The College of New Jersey.</p>

